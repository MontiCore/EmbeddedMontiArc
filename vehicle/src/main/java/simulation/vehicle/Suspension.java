package simulation.vehicle;

import org.javafmi.wrapper.Simulation;

/**
 * Contains a suspension.fmu and provides methods to safely interact with the fmu.
 */
public class Suspension {

    /** FMU that is represented with this class*/
    private Simulation suspension;

    /** Flag whether the FMU has been initialized */
    private boolean isInitialized = false;

    /** Flag whether the FMU has been terminated */
    private boolean isTerminated = false;

    /**
     * Constructor for an uninitialized suspension FMU
     */
    public Suspension(){
        suspension = new Simulation("lib/Suspension.fmu");
    }

    /**
     * Function that initialized the suspension FMU
     * Should only be called by physicalVehicleBuilder
     * @param startTime Start time of the calculations
     * @param stopTime Stop time of the calculation
     */
    public void initialize(double startTime, double stopTime){
        if(!isInitialized) {
            suspension.init(startTime, stopTime);
            isInitialized = true;
        }else{
            //ToDo error if already initialized
        }
    }

    /**
     * Function that executes one computation step
     * @param stepSize Size of the computation step
     */
    public void doStep(double stepSize){
        if(isInitialized) {
            if (!isTerminated) {
                suspension.doStep(stepSize);
            } else {
                //ToDo error if already terminated
            }
        }else{
            //ToDo error if not initialized
        }
    }

    /**
     * Function that terminated the computation
     */
    public void terminate(){
        if(isInitialized) {
            suspension.terminate();
            isTerminated = true;
        }else{
            //ToDo error if not initialized
        }
    }

    /**
     * Function that returns if the computation is terminated
     * @return Value of isTerminated
     */
    public boolean isTerminated(){
        isTerminated = suspension.isTerminated();
        return isTerminated;
    }

    /**
     * Function that returns the suspension FMU
     * @return Suspension FMU
     */
    public Simulation getSimulator(){
        return suspension;
    }
}
